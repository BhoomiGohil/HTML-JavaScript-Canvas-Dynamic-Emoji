function design(ctx) { // Call function to make design.
  ctx.fillStyle = "#615a5a";
  ctx.strokeStyle = "#615a5a";
  ctx.lineCap = "round";
  ctx.lineWidth = "3";
}

function measurement(canvas) { // Call to set width and height.
  canvas.width = canvas.height = 90;
}

